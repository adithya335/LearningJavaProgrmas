package june12thTask;

public class Typecasting {

    public static void main(String[] args) {
        int A=100;
        System.out.println((double)A);
        double B=100.0439;
        System.out.println((int)B);
        char C='C';
        System.out.println((int)C);
        int a=10;
        int b=3;
        double c= (double) a /b;
        System.out.println(c);
    }
}
